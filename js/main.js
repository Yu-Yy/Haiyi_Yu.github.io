// ========================================
// Language Management
// ========================================

const LANG_KEY = 'portfolio-lang';
let currentLang = localStorage.getItem(LANG_KEY) || 'zh';

function setLanguage(lang) {
  currentLang = lang;
  localStorage.setItem(LANG_KEY, lang);

  document.documentElement.lang = lang === 'zh' ? 'zh-CN' : 'en';

  document.querySelectorAll('[data-i18n]').forEach((el) => {
    const key = el.getAttribute('data-i18n');
    if (translations[lang] && translations[lang][key]) {
      el.textContent = translations[lang][key];
    }
  });

  document.querySelector('.hero-name-zh').classList.toggle('active', lang === 'zh');
  document.querySelector('.hero-name-en').classList.toggle('active', lang === 'en');

  document.querySelectorAll('.lang-option').forEach((opt) => {
    opt.classList.toggle('active', opt.getAttribute('data-lang') === lang);
  });
}

setLanguage(currentLang);

document.getElementById('langToggle').addEventListener('click', () => {
  const nextLang = currentLang === 'zh' ? 'en' : 'zh';
  setLanguage(nextLang);
});

// ========================================
// Scroll Reveal
// ========================================

const revealElements = document.querySelectorAll('.reveal');

const observer = new IntersectionObserver(
  (entries) => {
    entries.forEach((entry) => {
      if (entry.isIntersecting) {
        entry.target.classList.add('visible');
        observer.unobserve(entry.target);
      }
    });
  },
  {
    threshold: 0.15,
    rootMargin: '0px 0px -60px 0px',
  }
);

revealElements.forEach((el) => observer.observe(el));

// ========================================
// Header scroll effect
// ========================================

const header = document.getElementById('header');

function updateHeader() {
  if (window.scrollY > 40) {
    header.classList.add('scrolled');
  } else {
    header.classList.remove('scrolled');
  }
}

window.addEventListener('scroll', updateHeader, { passive: true });

// ========================================
// Dark Mode Toggle
// ========================================

const THEME_KEY = 'portfolio-theme';
const themeToggle = document.getElementById('themeToggle');

function applyTheme(dark) {
  document.documentElement.classList.toggle('dark', dark);
  if (dark) {
    themeToggle.querySelector('.theme-toggle-icon').textContent = '◑';
  } else {
    themeToggle.querySelector('.theme-toggle-icon').textContent = '◐';
  }
}

const prefersDark = window.matchMedia('(prefers-color-scheme: dark)').matches;
const savedTheme = localStorage.getItem(THEME_KEY);
const isDark = savedTheme !== null ? savedTheme === 'dark' : prefersDark;
applyTheme(isDark);

themeToggle.addEventListener('click', () => {
  const nextDark = !document.documentElement.classList.contains('dark');
  applyTheme(nextDark);
  localStorage.setItem(THEME_KEY, nextDark ? 'dark' : 'light');
});

// Follow system theme changes (when no manual preference is set)
window.matchMedia('(prefers-color-scheme: dark)').addEventListener('change', (e) => {
  if (localStorage.getItem(THEME_KEY) === null) {
    applyTheme(e.matches);
  }
});
